package modelo;

/**
 *
 * @author aseba
 */
public class Matriz {
    private int matrizId;
    private String ubicacion;
    private int presupuestos;
    private int gastos;
    private String estrategiasMarketing;

    public int getMatrizId() {
        return matrizId;
    }

    public void setMatrizId(int matrizId) {
        this.matrizId = matrizId;
    }

    public String getUbicacion() {
        return ubicacion;
    }

    public void setUbicacion(String ubicacion) {
        this.ubicacion = ubicacion;
    }

    public int getPresupuestos() {
        return presupuestos;
    }

    public void setPresupuestos(int presupuestos) {
        this.presupuestos = presupuestos;
    }

    public int getGastos() {
        return gastos;
    }

    public void setGastos(int gastos) {
        this.gastos = gastos;
    }

    public String getEstrategiasMarketing() {
        return estrategiasMarketing;
    }

    public void setEstrategiasMarketing(String estrategiasMarketing) {
        this.estrategiasMarketing = estrategiasMarketing;
    }
}
